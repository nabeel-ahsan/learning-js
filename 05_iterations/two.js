let score = 11; // Initializing a variable 'score' with value 11

// Using a do-while loop to execute a block of code at least once, then continue while a condition is true
do {
    console.log(`Score is ${score}`); // Logging the value of 'score'
    score++; // Incrementing 'score' by 1
} while (score <= 10); // Continuing the loop as long as 'score' is less than or equal to 10


